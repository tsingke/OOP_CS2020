#include <stdio.h>
#include "score.h"
#include <windows.h>


/*----------------------------------*
		Main Function
*-----------------------------------*/

int main()
{

	printf("******************************\n");
	printf("       学生成绩管理分析系统        \n");
	printf("         Qingke Zhang            \n");
	printf("******************************\n\n");


	/*-1.变量初始化-*/
	int N = 0;            //学生总数
	SS  *pstu = NULL;    //学生数组-结构体数组指针实现

	double meanScore = 0.0;
	double stdScore = 0.0;

	//2.读取学生信息
	pstu = readDataFromFile(&N);

	/*-3.计算学生总成绩（总成绩 = 0.2*平时成绩 + 0.8*期末成绩)--*/
	calcuScore(pstu, N);

	/*-4.根据学生成绩排名-*/
	sortScore(pstu, N);

	/*-5.按照排名输出学生信息-*/
	printOut(pstu, N);

	/*-6.计算学生成绩的均值和方差-*/
	statistic(pstu, N, &meanScore, &stdScore);
	/*cout << "学生成绩均值为: " << meanScore << " 方差为: " << stdScore << endl;*/
	printf("学生的平均成绩为:%.4f, 成绩标准方差为:%.4f\n", meanScore, stdScore);

	/*-7. 成绩搜索功能(根据学号搜索)-*/

	char stuid[20];
	printf("请输入要查询的学生的学号id= ");
	scanf("%s", stuid);
	search(pstu, N, stuid);


	/*-8.释放动态内存空间-*/
	free(pstu);


	system("pause");
	return 0;
}



